    private void botonRevisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRevisarActionPerformed
        
        
        // Boton de Revisar
        /*
            Boton con tres opciones
            Revisar Parqueo: Se revisa parqueo actual y se ven las personas multadas
            Revisar multas: Historial de multas
            
        */
        
        String[] options = {"Revisar Parqueo", "Revisar Multas", "Salida"};

        // Mostrar el cuadro de diálogo de opciones
        int choice = JOptionPane.showOptionDialog(
                null,
                "Seleccione una opción:",
                "Menú de Opciones",
                JOptionPane.DEFAULT_OPTION,
                JOptionPane.INFORMATION_MESSAGE,
                null,
                options,
                options[0]
        );
        
        if (choice == 0) { // Revisar Parqueo
            
            
            sistema.mostrarInsp(usuario.nombreUsuario, usuario.pin,1);
            
        } else if (choice == 1) { // Revisar Multas
            
                
           sistema.mostrarInsp(usuario.nombreUsuario, usuario.pin,4); 
            
        }else if(choice == 2){ //Salida
        
        
        }      
        
    }//GEN-LAST:event_botonRevisarActionPerformed

