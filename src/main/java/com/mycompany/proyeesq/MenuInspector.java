    private void botonReportesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonReportesActionPerformed
        // TODO add your handling code here:
        String[] options = {"Reportes Generales", "Mostrar todos los espacios","Mostrar solo los espacios ocupados ",
            "Mostrar solo los espacios vacíos ", "Salida"};

        // Mostrar el cuadro de diálogo de opciones
        int choice2 = JOptionPane.showOptionDialog(
                null,
                "Seleccione una opción:",
                "Menú de Opciones",
                JOptionPane.DEFAULT_OPTION,
                JOptionPane.INFORMATION_MESSAGE,
                null,
                options,
                options[0]
        );
        
        
        
        if (choice2 == 0) {  // Reportes Generales 
            
            sistema.mostrarInsp(usuario.nombreUsuario, usuario.pin,2); 
            
        } else if ( choice2 ==1){ // Mostrar todos los espacios
        
            sistema.mostrarInsp(usuario.nombreUsuario, usuario.pin,5); 
        
        }else if (choice2 == 2) { //Mostrar solo los espacios ocupados.
            
            sistema.mostrarInsp(usuario.nombreUsuario, usuario.pin,6);    
            
        } else if(choice2 == 3) { // Mostrar solo los espacios vacíos.
            
            sistema.mostrarInsp(usuario.nombreUsuario, usuario.pin,7);    
        
        } else if(choice2 == 4){ //Salida
        
        
        }         
    }//GEN-LAST:event_botonReportesActionPerformed




